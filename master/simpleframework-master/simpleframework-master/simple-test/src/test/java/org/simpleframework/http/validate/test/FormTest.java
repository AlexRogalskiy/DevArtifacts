         resp.setDescription("OK");
         resp.setValue("Content-Type", "text/html; charset=UTF-8");
         resp.setValue("Server", "Apache/1.3.27 (Unix) mod_perl/1.27");
         resp.setInteger("Content-Length", DATA.length);
